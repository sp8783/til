field = DATA.read.split("\n").map(&:chars)
H = field.size
W = field[0].size

antennes = Hash.new { |h, k| h[k] = [] }
H.times do |i|
  W.times do |j|
    point = field[i][j]
    next if point == "."
    antennes[point] << [i, j]
  end
end

ans = 0
antennes.each do |k, v|
  next if v.size < 2
  v.combination(2).each do |(x1, y1), (x2, y2)|
    vec = [x2 - x1, y2 - y1]

    # 正の方向
    p1 = [x2, y2]
    while (p1[0] + vec[0]).between?(0, H - 1) && (p1[1] + vec[1]).between?(0, W - 1)
      p1 = [p1[0] + vec[0], p1[1] + vec[1]]
      if field[p1[0]][p1[1]] != "#"
        ans += 1
        field[p1[0]][p1[1]] = "#"
      end
    end

    # 負の方向
    p2 = [x1, y1]
    while (p2[0] - vec[0]).between?(0, H - 1) && (p2[1] - vec[1]).between?(0, W - 1)
      p2 = [p2[0] - vec[0], p2[1] - vec[1]]
      if field[p2[0]][p2[1]] != "#"
        ans += 1
        field[p2[0]][p2[1]] = "#"
      end
    end
  end
end

# 残ったアンテナを足す
ans += field.flatten.count { |c| c != "." && c != "#" }

# field.each { |row| puts row.join }
puts ans

